#include "main.h"
/**
 * _memcpy - function that copies memory area
 * @dest: memory where the value is stored
 * @src : number of bytes from memory area
 * @n: nber od bytes to be copied
 *
 * Return: a pointer to dest with n bytes changed
 */
char *_memcpy(char *dest, char *src, unsigned int n)
{
	unsigned int j = 0;

	while (j < n)
	{
		dest[j] = src[j];
		j++;
	}
	return (dest);
}
