#include "main.h"

/**
 * add - Function that add two numbers.
 * @a: The first number.
 * @b: The second number.
 *
 * Return: The result of the addition.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Function that subtract two numbers.
 * @a: The first number
 * @b: The second numbr
 *
 * Return: The result of the subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Function that multiply two numbers.
 * @a: The first number
 * @b: The second number.
 *
 * Return: The result of the multiplication.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Function that divide two numbers.
 * @a: The first number.
 * @b: The second number.
 *
 * Return: The result of the division.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Function that find modulus of two numbers.
 * @a: The first number.
 * @b: The second number.
 *
 * Return: The result of the modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
